<?php

$num1 = 5;
$num2 = 3;

// penumlahan
$result = $num1 + $num2;
var_dump($result);

// pengurangan
$result = $num1 - $num2;
var_dump($result);

// perkalian
$result = $num1 * $num2;
var_dump($result);

// pembagian
$result = $num1 / $num2;
var_dump($result);

// modulo
$result = $num1 % $num2;
var_dump($result);

// pangkat
$result = $num1 ** $num2;
var_dump($result);

?>